package hellojpa;

import javax.persistence.Entity;

@Entity
public class Book extends ItemTemp {

  private String author;
  private String isbn;

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }
}
